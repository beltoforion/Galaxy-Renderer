	static inline unsigned int PowerTwoFloor(unsigned int val)
	{
		unsigned int power = 2, nextVal = power * 2;

		while ((nextVal *= 2) <= val)
		{
			power = power << 1; // *= 2;
		}

		return power << 1; // power * 2;
	}

